package com.project1.admin.DTO;

public class PeriodDTO {

	int periodCode;
	int periodNum;
	int curriculumCode;
	int dayCode;
	public int getPeriodCode() {
		return periodCode;
	}
	public void setPeriodCode(int periodCode) {
		this.periodCode = periodCode;
	}
	public int getPeriodNum() {
		return periodNum;
	}
	public void setPeriodNum(int periodNum) {
		this.periodNum = periodNum;
	}
	public int getCurriculumCode() {
		return curriculumCode;
	}
	public void setCurriculumCode(int curriculumCode) {
		this.curriculumCode = curriculumCode;
	}
	public int getDayCode() {
		return dayCode;
	}
	public void setDayCode(int dayCode) {
		this.dayCode = dayCode;
	}
	
	
	
	
	
	
	
}
