package com.project1.admin.DTO;

public class FacultyDTO {

	private int 	staffCode;
	private String	staffName;
	private String	betweenCode;
	private String	password;
	private String	email;
	private int 	classCode;
	
	
	
	public int getStaffCode() {
		return staffCode;
	}
	public void setStaffCode(int staffCode) {
		this.staffCode = staffCode;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getBetweenCode() {
		return betweenCode;
	}
	public void setBetweenCode(String betweenCode) {
		this.betweenCode = betweenCode;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getClassCode() {
		return classCode;
	}
	public void setClassCode(int classCode) {
		this.classCode = classCode;
	}
	
	
	
	
}
