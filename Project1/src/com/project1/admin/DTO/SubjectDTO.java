package com.project1.admin.DTO;

public class SubjectDTO {

	private int subjectCode;
	private String subjectName;
	private String subjectScore;
	private String period;
	private String subjectProgram;
	private int classCode;
	
	
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	
	public int getSubjectCode() {
		return subjectCode;
	}
	public void setSubjectCode(int subjectCode) {
		this.subjectCode = subjectCode;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public String getSubjectScore() {
		return subjectScore;
	}
	public void setSubjectScore(String subjectScore) {
		this.subjectScore = subjectScore;
	}
	public String getSubjectProgram() {
		return subjectProgram;
	}
	public void setSubjectProgram(String subjectProgram) {
		this.subjectProgram = subjectProgram;
	}
	public int getClassCode() {
		return classCode;
	}
	public void setClassCode(int classCode) {
		this.classCode = classCode;
	}
	
	
	
	
	
}
